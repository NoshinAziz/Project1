public class Calculator {

    public void openCalculator(){
        System.out.println("Open my Calculator to do Math");
    }
    public int addTwoNumber(int a, int b){
        return a+b;
    }
    public int deductTwoNumber(int a, int b){
        return a-b;
    }
    public int addThreeNumber(int a, int b, int c){
        return a+b+c;
    }
    public int deductThreeNumber(int a, int b,int c){
        return a-b-c;
    }



}
